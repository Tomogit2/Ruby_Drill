問題 39 条件演算子

問題.1
3桁の正の整数を入力します。その整数の「百の位・十の位・一の位の和」について、
10の倍数（10,20,30...）からの差が
・2以内であるときは"True"
・それ以外は"10の倍数との差は○です"
と表示されるようにしましょう。

出力例：
near_ten(117)→True
near_ten(123)→10の倍数との差は4です
near_ten(111)→10の倍数との差は3です

百の位・十の位・一の位の和が6などの時に、「10の倍数との差は6です」と出力せずに、「10の倍数との差は4です」と10の倍数から近い方の差を出力するようにしてください。また、0も10の倍数に含むものとします。
----------------------------------------

def near_ten(num)
  # 百の位、十の位、一の位を計算
  hundreds = num / 100
  tens = (num % 100) / 10
  ones = num % 10

  # 百の位、十の位、一の位の和を計算
  sum = hundreds + tens + ones

  # 10の倍数との差を計算
  remainder = sum % 10
  difference = [remainder, 10 - remainder].min

  # 差が2以内ならTrue、それ以外なら差を表示
  if difference <= 2
    puts "True"
  else
    puts "10の倍数との差は#{difference}です"
  end
end

# ユーザー入力
puts "3桁の正の整数を入力してください:"
num = gets.to_i

# 関数を実行
near_ten(num)
